- event_action：只写一个 2-8 字原子动作词，优先使用：计划开店、正式开店、计划关店、关闭门店、任命高管、高管离任、增持股份、减持股份、发布业绩、融资上市、计划入股、计划收购、完成收购、启动合作、终止合作、上线功能、发布产品、价格上涨、价格下调、监管处置、争议维权、捐赠救援、获得奖项
